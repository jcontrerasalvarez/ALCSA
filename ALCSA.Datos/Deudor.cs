            objServicio.Comando = "dbo.SPALC_DEUDORES_BUSCAR";
            objServicio.Parametros.Add(new ALCSA.FWK.BD.Parametro() { Nombre = "@VC_RutDeudor", Valor = rut, Direccion = ALCSA.FWK.BD.Enumeradores.Direcciones.Entrada });
