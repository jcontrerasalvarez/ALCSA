        public ALCSA.Entidades.Juicios.Juicio Buscar(int idJuicio, int idCobranza)
        {
            ALCSA.FWK.BD.Servicio objServicio = new ALCSA.FWK.BD.Servicio();
            objServicio.Conexion = Conexion.ALCSA;
            objServicio.Comando = "dbo.SPALC_JUICIOS_BUSCAR";
            objServicio.Parametros.Add(new ALCSA.FWK.BD.Parametro() { Nombre = "@INT_IdCobranza", Valor = idCobranza, Direccion = ALCSA.FWK.BD.Enumeradores.Direcciones.Entrada });
            objServicio.Parametros.Add(new ALCSA.FWK.BD.Parametro() { Nombre = "@INT_IdJuicios", Valor = idJuicio, Direccion = ALCSA.FWK.BD.Enumeradores.Direcciones.Entrada });
            IList<ALCSA.Entidades.Juicios.Juicio> arrRespuesta = objServicio.Ejecutar<ALCSA.Entidades.Juicios.Juicio>();
            return arrRespuesta.Count > 0 ? arrRespuesta[0] : null;
        }

